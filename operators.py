# Python Logical Operators => Logical operators are used to combine conditional statements

# and #(Returns True if both statements are true)
# or #(Returns True if one of the statements is true)
# not #(Reverse the result, returns False if the result is true)

# Python Identity Operators => are used to compare the objects, not if they are equal, but if they are actually the same object, with the same memory location:

# x is y #(Returns True if both variables are the same object)
# x is not y #(Returns True if both variables are not the same object)

# Python Membership Operators => are used to test if a sequence is presented in an object:

# x = 'Francisco'
# y = 'isco'

# x in y #(Returns True if a sequence with the specified value is present in the object)

# x not in y #(Returns True if a sequence with the specified values is not present in the object)


